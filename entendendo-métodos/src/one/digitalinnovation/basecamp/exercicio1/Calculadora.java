package one.digitalinnovation.basecamp.exercicio1;

public class Calculadora {

    public static void soma(int value1, int value2) {
        System.out.println("Valor da soma: " + (value1 + value2));
    }

    public static void subtracao(int value1, int value2) {
        System.out.println("Valor da subtração: " + (value1 - value2));
    }

    public static void multiplicacao(int value1, int value2) {
        System.out.println("Valor da multiplicação: " + (value1 * value2));
    }

    public static void divisao(int value1, int value2) {
        System.out.println("Valor da divisão: " + (value1 / value2));
    }
}
